    /**
     * Removes JPanels of dealer and player to restart game
     * Sets xAxis to default values
     * Clears notification bar
     * Enables all buttons
     */
    public void restartGame(){
        playerCardJPanel.removeAll();
        playerSplitCardJPanel.removeAll();
        dealerCardJPanel.removeAll();
        playerCardJPanel.removeAll();

        playerCardJPanel.revalidate();
        playerCardJPanel.repaint();

        playerSplitCardJPanel.revalidate();
        playerSplitCardJPanel.repaint();

        dealerCardJPanel.revalidate();
        dealerCardJPanel.repaint();

        xAxisPlayer = 197;
        xAxisSplitPlayer = 0;
        xAxisDealer = 0;
        menuNotifications.setText("");
        betPlaced = false;
        menuPlaceBetButton.setEnabled(true);
    }


