    private void menuPlaceBetButtonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_menuPlaceBetButtonMousePressed
        if (menuPlaceBetButton.isEnabled()) {
            menuPlaceBetButton.setIcon(new javax.swing.ImageIcon(getClass().getClassLoader().getResource("PlaceBetPRESSED.png"))); // NOI18N
            double bet = Double.parseDouble(menuBetInputTF.getText());
            if (bet <= Double.parseDouble(gameRunner.getPlayerData("balance")) && bet >= 1) {
                WavPlayer.startEffect("src/main/resources/chipsSettle.wav");
                menuPlaceBetButton.setEnabled(false);
                gameRunner.firstBet(bet);
                betPlaced = true;
                updateBalance();
            } else {
                menuPlaceBetButton.setIcon(new javax.swing.ImageIcon(getClass().getClassLoader().getResource("PlaceBet.png"))); // NOI18N
                JOptionPane.showMessageDialog(this, "Balance is too low or you bet below the minimum", "BlackJack", JOptionPane.PLAIN_MESSAGE);
            }

        }
    }//GEN-LAST:event_menuPlaceBetButtonMousePressed

